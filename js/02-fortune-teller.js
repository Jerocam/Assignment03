//FUNCTION CALL TELLFORTUNE WITH 4 PARAMETERS
function tellFortune(children,partnerName,gplace,jobTitle){
    "use strict";
    document.write("You will be a "+jobTitle+" in "+gplace+", and married to "+partnerName+" with " +children+" kids.<br>"); //it displays with the results from values
}

//VALUES TO CALL FUNCTION TELLFORTUNE
tellFortune (3,"Maria","San Diego","Graphic Designer");
tellFortune (9,"Jorge","Dallas","Lawyer");
tellFortune (2,"Pablo","Miami City","Mechanic");
